/*  JavaScript é uma linguagem de programação amplamente utilizada, especialmente no desenvolvimento web. Ela foi criada originalmente para adicionar interatividade e recursos dinâmicos a páginas da web. Com o passar do tempo, o JavaScript se tornou uma linguagem de programação completa, 
    capaz de executar tarefas complexas tanto no lado do cliente (no navegador) quanto no lado do servidor.
    No contexto da web, o JavaScript é principalmente utilizado para manipular elementos HTML, controlar a aparência e o comportamento de páginas, lidar com eventos, validar formulários, criar animações, interagir com APIs (Interfaces de Programação de Aplicativos) e muito mais. 
    Ele permite que os desenvolvedores criem aplicativos web interativos e dinâmicos.
*/


/*
Você pode ouvir os termos interpretado e compilado no contexto da programação. JavaScript é uma linguagem interpretada — 
 o código é executado de cima para baixo e o resultado da execução do código é imediatamente retornado. Você não tem que transformar o código em algo diferente antes do navegador executa-lo.

Linguagens compiladas, por outro lado, são transformadas (compiladas) em algo diferente antes que sejam executadas pelo computador. 
Por exemplo, C/C++ são compiladas em linguagem Assembly, e depois são executadas pelo computador.


*/